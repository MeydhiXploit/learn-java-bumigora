public class forCabang {
    public static void main(String[] args) {
        for (int i = 2; i <= 5; i++) {
                System.out.println("");
            for (int j = 5; j >= i; j--) {
                System.out.print(j);
            }
        }
        for (int i = 5; i >= 1; i--) {
            System.out.println("");
            for (int j = 5; j >= i; j--) {
                System.out.print(j);
            }
        }
        System.out.println("");
    }
  
}

